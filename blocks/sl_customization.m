function sl_customization(cm)
    % Change the order of libraries in the Simulink Library Browser. 
    cm.LibraryBrowserCustomizer.applyOrder( ...
        {'Neuromorphic Control Toolbox/Neural Blocks',-5 ...
        ,'Neuromorphic Control Toolbox/Auxiliary Blocks',-4 ...
        ,'Neuromorphic Control Toolbox/Events Transmission Blocks',-3 ...
        ,'Neuromorphic Control Toolbox/Quick Insert',-2 ...
        ,'Neuromorphic Control Toolbox/Quick Insert/Neural Blocks',-5 ...
        ,'Neuromorphic Control Toolbox/Quick Insert/Secondary Blocks',-4 ...
        ,'Neuromorphic Control Toolbox/Quick Insert/Events Transmission Blocks',-3} ...
    );
end

